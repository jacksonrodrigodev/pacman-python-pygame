pontuacoes = {}

clients = set()

jogadores_conectados = {}


async def processar_movimento(mensagem, player_id):
    linha = mensagem["linha"]
    coluna = mensagem["coluna"]

    if matriz_global[linha][coluna] == 1:
        matriz_global[linha][coluna] = 0
        pontuacoes[player_id] = pontuacoes.get(player_id, 0) + 1
    else:
        pontuacoes.setdefault(player_id, 0)

    resposta = {
        "tipo": "estado_jogo",
        "jogador_id": player_id,
        "linha": linha,
        "coluna": coluna,
        "pontos": pontuacoes[player_id],
        "matriz": matriz_global,
    }

    for client in clients.copy():
        try:
            await client.send_text(json.dumps(resposta))
        except:
            clients.remove(client)
            print("‚ùå Cliente desconectado durante envio")


@app.websocket("/ws")
async def websocket_endpoint(websocket: WebSocket):
    await websocket.accept()
    print("üü¢ Novo cliente conectado")

    try:
        data = await asyncio.wait_for(websocket.receive_text(), timeout=10)
        mensagem = json.loads(data)

        if mensagem["tipo"] != "movimento" or "id" not in mensagem:
            await websocket.send_text(
                json.dumps({"tipo": "erro", "mensagem": "Formato inv√°lido"})
            )
            await websocket.close(code=1003)
            return

        id_jogador = mensagem["id"]

        if len(jogadores_conectados) >= 2:
            await websocket.send_text(
                json.dumps(
                    {"tipo": "erro", "mensagem": "N√∫mero m√°ximo de jogadores atingido."}
                )
            )
            await websocket.close(code=1000)
            return

        jogadores_conectados[id_jogador] = websocket
        clients.add(websocket)

        await websocket.send_text(
            json.dumps({"tipo": "estado_matriz", "matriz": matriz_global})
        )

        await processar_movimento(mensagem, id_jogador)

        while True:
            try:
                data = await asyncio.wait_for(websocket.receive_text(), timeout=30)
            except asyncio.TimeoutError:
                print("‚è≥ Cliente inativo por 30s, desconectando...")
                break

            mensagem = json.loads(data)

            if mensagem["tipo"] == "movimento":
                await processar_movimento(mensagem, id_jogador)

    except Exception as e:
        print("‚ùå Cliente desconectado:", e)
    finally:
        clients.discard(websocket)
        if "id_jogador" in locals():
            jogadores_conectados.pop(id_jogador, None)
            print(f"üëã Jogador {id_jogador} desconectado")

            saida_msg = {"tipo": "jogador_saiu", "jogador_id": id_jogador}

            for client in clients.copy():
                try:
                    await client.send_text(json.dumps(saida_msg))
                except:
                    clients.discard(client)
                    print("‚ùå Erro ao notificar cliente da sa√≠da")


